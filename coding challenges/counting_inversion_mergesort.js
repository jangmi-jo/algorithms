let numSwap = 0;

const mergeSort = (arr) => {
  if (arr.length <= 1) {
    return arr;
  }
  let mid = Math.floor(arr.length / 2);
  let left = arr.slice(0, mid);
  let right = arr.slice(mid);
  return merge(mergeSort(left), mergeSort(right));
};

const merge = (a, b) => {
  let res = [];
  while (a.length && b.length) {
    if (a[0] <= b [0]) {
      res.push(a.shift());
    } else {
      numSwap++;
      res.push(b.shift());
    }
  }
  return res.concat(a, b);
};
